import type { Trail, PoiType } from './types';

// Linear interpolation path between two GPS points
function lp(
  from: [number, number],
  to: [number, number],
  steps = 12
): { lat: number; lng: number }[] {
  const pts = [];
  for (let i = 0; i <= steps; i++) {
    const t = i / steps;
    pts.push({ lat: from[0] + t * (to[0] - from[0]), lng: from[1] + t * (to[1] - from[1]) });
  }
  return pts;
}

type WptDef = [number, number, string, PoiType];

function mk(
  id: string,
  name: string,
  difficulty: Trail['difficulty'],
  distKm: number,
  elevM: number,
  waypoints: WptDef[]
): Trail {
  const first = waypoints[0];
  const last = waypoints[waypoints.length - 1];
  // Build path: chain lp segments through each waypoint
  let points: { lat: number; lng: number }[] = [];
  for (let i = 0; i < waypoints.length - 1; i++) {
    const seg = lp([waypoints[i][0], waypoints[i][1]], [waypoints[i + 1][0], waypoints[i + 1][1]], 8);
    points = [...points, ...(i === 0 ? seg : seg.slice(1))];
  }
  return {
    id,
    name,
    difficulty,
    totalDistanceKm: distKm,
    elevationGainM: elevM,
    walkedPoints: [],
    walkedDistanceKm: 0,
    completionPercent: 0,
    importedAt: '2026-01-01T00:00:00.000Z',
    waypoints: waypoints.map((w, i) => ({
      id: `${id}-w${i}`,
      name: w[2],
      position: { lat: w[0], lng: w[1] },
      poiType: w[3],
    })),
    points,
  };
}

